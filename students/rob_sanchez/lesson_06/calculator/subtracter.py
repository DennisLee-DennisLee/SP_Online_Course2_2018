""" This module provides the Subtracter operator. """


class Subtracter(object):
    ''' This is the Subtracter class that returns the
    addition of the input given '''

    @staticmethod
    def calc(operand_1, operand_2):
        ''' Returns the subtraction of operand_1 and operand_2 '''
        return operand_1 - operand_2
