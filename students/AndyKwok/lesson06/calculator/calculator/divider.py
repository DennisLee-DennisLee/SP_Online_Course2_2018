"""
This module provides a division operator
"""


class Divider:
    """
    Class for dividing
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Perform division
        """
        return operand_1 / operand_2

    @staticmethod
    def dummy_calc(operand_1, operand_2):
        """
        Dummy division
        """
        return operand_1 / operand_2
