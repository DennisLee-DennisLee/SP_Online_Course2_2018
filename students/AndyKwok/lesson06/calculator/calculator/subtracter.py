"""
This module provides a subtraction operator
"""


class Subtracter:
    """
    Class for subtracting
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Performs subraction
        """
        return operand_1 - operand_2

    @staticmethod
    def dummy_calc(operand_1, operand_2):
        """
        Dummy subraction
        """
        return operand_1 - operand_2
