"""
This module provides a multiply operator
"""


class Multiplier:
    """
    Class for multiplying
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Perform multiplication
        """
        return operand_1 * operand_2

    @staticmethod
    def dummy_calc(operand_1, operand_2):
        """
        Dummy multiplication
        """
        return operand_1 * operand_2
