"""
This module provides a addition operator
"""


class Adder:
    """
    Class for adding
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Performs addition
        """
        return operand_1 + operand_2

    @staticmethod
    def dummy_calc(operand_1, operand_2):
        """
        Dummy addition
        """
        return operand_1 + operand_2
